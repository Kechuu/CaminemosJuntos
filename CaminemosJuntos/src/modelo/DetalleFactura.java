package modelo;

import java.sql.Date;

/**
 *
 * @author javier
 */
public class DetalleFactura {
    private int idDetalleFactura;
    private Factura factura;
    private int mes;
    private int year;
    private float importe;
    private AlumnoActividad alumnoActividad;
    private Date fechaPago;

    public int getIdDetalleFactura() {
        return idDetalleFactura;
    }

    public void setIdDetalleFactura(int idDetalleFactura) {
        this.idDetalleFactura = idDetalleFactura;
    }

    public Factura getFactura() {
        return factura;
    }

    public void setFactura(Factura factura) {
        this.factura = factura;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public float getImporte() {
        return importe;
    }

    public void setImporte(float importe) {
        this.importe = importe;
    }

    public AlumnoActividad getAlumnoActividad() {
        return alumnoActividad;
    }

    public void setAlumnoActividad(AlumnoActividad alumnoActividad) {
        this.alumnoActividad = alumnoActividad;
    }

    public Date getFechaPago() {
        return fechaPago;
    }

    public void setFechaPago(Date fechaPago) {
        this.fechaPago = fechaPago;
    }
}
