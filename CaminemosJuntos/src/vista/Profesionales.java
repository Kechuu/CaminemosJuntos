    private void cargarTelefonos(List<Telefono> tlfn)
    {
        
        String telefono[] = new String[3];
        
        modeloTelefonos.setRowCount(0);
        for(int i = 0; i < tlfn.size(); i++)
        {
            telefono[0] = tlfn.get(i).getTipoTelefono().getTipoTelefono();
            telefono[1] = tlfn.get(i).getCodigoArea();
            telefono[2] = tlfn.get(i).getNumeroTelefono();
            modeloTelefonos.addRow(telefono);
        }
        tblTelefonos.setModel(modeloTelefonos);
    }

    private void limpiarDatos()
    {
        txtDNI.setText("");
        txtApellido.setText("");
        txtNombre.setText("");
        txtFechaNacimiento.setDate(new java.util.Date());
        optHombre.setSelected(false);
        optMujer.setSelected(false);
        optOtro.setSelected(false);
        limpiarCombo(cbxProvincia);
        cargarCombo(cbxProvincia, ctrlLugarDomicilio.leerTodos(5));
        limpiarCombo(cbxDepartamento);
        limpiarCombo(cbxLocalidad);
        limpiarCombo(cbxBarrio);
        limpiarCombo(cbxCalle);
        txtNro.setText("");
        txtTorre.setText("");
        txtPiso.setText("");
        txtDepto.setText("");
    }
    
    private void cargarCombo(JComboBox cbx, List<String> lst)
    {
        cbx.removeAllItems();
        cbx.addItem("Seleccione...");
        if(lst != null)
        {
            for(int i=0; i<lst.size(); i++)
            {
                cbx.addItem(lst.get(i));
            }
        }
    }

    private void limpiarCombo(JComboBox cbx)
    {
        cbx.removeAllItems();
        cbx.addItem("Seleccione...");
    }

