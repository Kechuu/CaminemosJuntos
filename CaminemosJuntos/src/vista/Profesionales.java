    private boolean validarDatos()
    {
        boolean valida = true;
        
        if(cbxTipoDoc.getSelectedIndex() == 0 || txtDNI.getText().equalsIgnoreCase(""))
            valida = false;
        else if(txtApellido.getText().equalsIgnoreCase("") || txtNombre.getText().equalsIgnoreCase(""))
                valida = false;
            else if(!optHombre.isSelected() && !optMujer.isSelected() && !optOtro.isSelected())
                    valida = false;
                else if(cbxProvincia.getSelectedIndex() == 0 || cbxDepartamento.getSelectedIndex() == 0 || cbxLocalidad.getSelectedIndex() == 0 || cbxBarrio.getSelectedIndex() == 0 || 
                        cbxCalle.getSelectedIndex() == 0 || txtNro.getText().equalsIgnoreCase(""))
                        valida = false;
                    if(tblTelefonos.getRowCount() == 0)
                    {
                        valida = false;
                        JOptionPane.showMessageDialog(this, "Debe registrar al menos un teléfono del Profesional", "ATENCIÓN...", INFORMATION_MESSAGE);
                    }    
        if(!valida)
            JOptionPane.showMessageDialog(this, "Hay datos obligatorios del Profesional sin completar", "ATENCION...", ERROR_MESSAGE);
        
        return valida;
    }
    
