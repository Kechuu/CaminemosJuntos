    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        if(modifica)
        {
            estadoProfesional.setEstadoProfesional(txtNuevo.getText().trim().toUpperCase());

            ctrlEstadoProfesional.editar(estadoProfesional);
            
            lblModificando.setVisible(false);
            modifica = false;
        }
        else
        {
            lugar = new LugarDomicilio();
            lugar.setNombre(txtNuevo.getText().trim().toUpperCase());
            lugar.setNivel(4);
            lugar.setDe(prvnc);

            ctrlLugar.crear(lugar);
        }
        txtNuevo.requestFocus();
        txtNuevo.setText("");
        cargarTabla();
    }//GEN-LAST:event_btnGrabarActionPerformed

