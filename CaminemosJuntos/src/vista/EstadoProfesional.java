    public void cargarTabla()
    {
        String[] fila = new String[1];
        List<String> listaEstados = null;
        
        if(prvnc != null)
           listaLugares = ctrlLugar.leerTodosDe(prvnc);

        modeloTabla.setColumnCount(0);
        modeloTabla.setRowCount(0);
        modeloTabla.addColumn("DEPARTAMENTOS");

        if(listaLugares != null)
            for(int i=0; i<listaLugares.size(); i++)
            {
                fila[0] = listaLugares.get(i);
                modeloTabla.addRow(fila);
            }
            tabla.setModel(modeloTabla);
    }
    
    public void cargarCombo(JComboBox cbx, List lst)
    {
        cbx.removeAllItems();
        for(int i=0; i<lst.size(); i++)
        {
            cbx.addItem(lst.get(i).toString());
        }
    }


