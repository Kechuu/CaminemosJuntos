    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        if(evt.getClickCount()==2)
        {
            tipoDocumento = ctrlTipoDocumento.leer(tabla.getValueAt(tabla.getSelectedRow(), 0).toString());
            txtNombreCorto.setText(tipoDocumento.getNombreCorto());
            txtNombreCompleto.setText(tipoDocumento.getTipoDocumento());
            
            txtNombreCorto.requestFocus();
        }
    }//GEN-LAST:event_tablaMouseClicked

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Â¿ELIMINAR "+txtNombreCorto.getText().toUpperCase()+"?", "CONFIRMAR", YES_NO_OPTION, QUESTION_MESSAGE) == YES_OPTION)
        {    
            ctrlTipoDocumento.borrar(tipoDocumento.getIdtipoDocumento());

            txtNombreCorto.requestFocus();
            limpiarDatos();            
            cargarTabla();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "LA OPERACION HA SIDO CANCELADA", "CANCELADO", INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    public void cargarTabla()
    {
        String[] fila = new String[2];
        List<TipoDocumento> lista = new ArrayList();

        lista = ctrlTipoDocumento.leerTodosCompleto();
        modeloTabla.setColumnCount(0);
        modeloTabla.setRowCount(0);
        modeloTabla.addColumn("NOMBRE CORTO");
        modeloTabla.addColumn("NOMBRE COMPLETO");

        for(int i=0; i<lista.size(); i++)
        {
            fila[0] = lista.get(i).getNombreCorto();
            fila[1] = lista.get(i).getTipoDocumento();
            modeloTabla.addRow(fila);
        }
        tabla.setModel(modeloTabla);
    }

    private void datosON()
    {
        txtNombreCompleto.setEditable(true);
    }
    
    private void datosOFF()
    {
        txtNombreCompleto.setEditable(false);
    }

    private void limpiarDatos()
    {
        txtNombreCorto.setText("");
        txtNombreCompleto.setText("");
    }

    private boolean validarDatos()
    {
        boolean valida = true;
        
        if(txtNombreCorto.getText().equals(""))
        {
            valida = false;
            JOptionPane.showMessageDialog(null, "Ingrese el Nombre Corto del Tipo de Documento");
        }
        if(txtNombreCompleto.getText().equals(""))
        {
            valida = false;
            JOptionPane.showMessageDialog(null, "Ingrese el Nombre Completo del Tipo de Documento");
        }
        return valida;
    }
