    private void txtNombreCortoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNombreCortoFocusLost
        if(!txtNombreCorto.getText().equals(""))
        {
            tipoDocumento = ctrlTipoDocumento.leer(txtNombreCorto.getText());
            if(tipoDocumento == null || tipoDocumento.isBorrado())
            {
                datosON();
                btnGrabar.setEnabled(true);
                btnModificar.setEnabled(false);
                btnBorrar.setEnabled(false);
            }
            else
            {
                if(modifica && tipoDocumento.getIdtipoDocumento() == tipoDocumentoModificado.getIdtipoDocumento())
                {
                    datosON();
                    btnGrabar.setEnabled(true);
                    btnModificar.setEnabled(false);
                    btnBorrar.setEnabled(false);
                }
                else
                {
                    txtNombreCompleto.setText(tipoDocumento.getTipoDocumento());
                    lblModificando.setVisible(false);

                    btnGrabar.setEnabled(false);
                    btnModificar.setEnabled(true);
                    btnBorrar.setEnabled(true);
                }
            }
        }
    }//GEN-LAST:event_txtNombreCortoFocusLost

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        tipoDocumentoModificado = tipoDocumento;
        modifica = true;
        txtNombreCorto.requestFocus();
        datosON();
        lblModificando.setText("*Modificando "+txtNombreCorto.getText());
        lblModificando.setVisible(true);
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        if(validarDatos())
        {
            if(modifica)
            {
                tipoDocumentoModificado.setNombreCorto(txtNombreCorto.getText().trim().toUpperCase());
                tipoDocumentoModificado.setTipoDocumento(txtNombreCompleto.getText().trim().toUpperCase());
                tipoDocumentoModificado.setBorrado(false);

                ctrlTipoDocumento.editar(tipoDocumentoModificado);

                lblModificando.setVisible(false);
                modifica = false;
            }
            else
            {
                if(tipoDocumento != null)
                {
                    ctrlTipoDocumento.recuperar(tipoDocumento.getIdtipoDocumento());

                    tipoDocumento.setNombreCorto(txtNombreCorto.getText().trim().toUpperCase());
                    tipoDocumento.setTipoDocumento(txtNombreCompleto.getText().trim().toUpperCase());
                    tipoDocumento.setBorrado(false);

                    ctrlTipoDocumento.editar(tipoDocumento);
                }
                else
                {
                    tipoDocumento = new TipoDocumento();
                    tipoDocumento.setNombreCorto(txtNombreCorto.getText().trim().toUpperCase());
                    tipoDocumento.setTipoDocumento(txtNombreCompleto.getText().trim().toUpperCase());
                    tipoDocumento.setBorrado(false);
                    ctrlTipoDocumento.crear(tipoDocumento);
                }
            }
            txtNombreCorto.requestFocus();
            txtNombreCorto.setText("");
            limpiarDatos();
            datosOFF();
            cargarTabla();
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

